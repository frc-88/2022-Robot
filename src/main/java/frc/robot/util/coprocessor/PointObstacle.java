package frc.robot.util.coprocessor;

public class PointObstacle {
    private double x;
    private double y;

    public PointObstacle() {
        x = 0.0;
        y = 0.0;
    }

    public PointObstacle(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void set(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
